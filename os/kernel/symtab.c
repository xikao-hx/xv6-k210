int symtab_len = sizeof(symtab) / sizeof(struct symtab_entry);

struct symtab_entry* lookup_sym(uint64 addr) {
  struct symtab_entry* match = 0;
  for (int i = 0; i < symtab_len; i++) {
    if (symtab[i].addr <= addr) {
      if (match == 0 || symtab[i].addr > match->addr) {
        match = &symtab[i];
      }
    }
  }
  return match;
}